let activeCard = null;
let isClosing = false;
let activeFilters = { gender: "", race: "", location: "", npc: "characters" };
let cardsContainer = null;
let buttonsContainer = null;
let backgroundElement = null;
let backButton = null;

const backgroundMap = {
    "город": "images/city.jpg",
    "сектор": "images/sector.jpg",
    "элита": "images/elite.jpg",
    "безопасность": "images/safe.jpg"
};

function changeBackgroundByLocation(locationValue) {
    if (!backgroundElement) return;
    
    if (locationValue && backgroundMap[locationValue]) {
        backgroundElement.style.backgroundImage = `url('${backgroundMap[locationValue]}')`;
        backgroundElement.style.backgroundSize = "cover";
        backgroundElement.style.backgroundPosition = "center";
        backgroundElement.classList.remove("blurred");
        backgroundElement.classList.add("unblurred");
    } else {
        backgroundElement.style.backgroundImage = "url('images/city.jpg')";
        backgroundElement.style.backgroundSize = "cover";
        backgroundElement.style.backgroundPosition = "center";
        backgroundElement.classList.remove("unblurred");
        backgroundElement.classList.add("blurred");
    }
}

function getFilteredCharacters() {
    const charKeys = Object.keys(charactersData);
    
    return charKeys.filter(key => {
        const data = charactersData[key];
        if (!data) return false;
        
        if (activeFilters.npc === "characters" && data.isNPC === true) return false;
        if (activeFilters.npc === "npcs" && data.isNPC === false) return false;
        
        if (activeFilters.gender && data.gender !== activeFilters.gender) return false;
        if (activeFilters.race && data.type !== activeFilters.race) return false;
        if (activeFilters.location && data.location !== activeFilters.location) return false;
        
        return true;
    });
}

function renderButtons() {
    if (!buttonsContainer) return;
    
    const filteredKeys = getFilteredCharacters();
    
    
    const sortedKeys = [...filteredKeys].sort((a, b) => {
        const nameA = charactersData[a].buttonName.toLowerCase();
        const nameB = charactersData[b].buttonName.toLowerCase();
        return nameA.localeCompare(nameB, 'ru');
    });
    
    buttonsContainer.innerHTML = '';
    
    for (let key of sortedKeys) {
        const data = charactersData[key];
        const button = document.createElement('div');
        button.className = 'character-button';
        button.setAttribute('data-character', key);
        button.innerHTML = `
            <div class="btn-gothic">
                <div class="btn-text">${data.buttonName}</div>
                <div class="btn-sub">${data.displayType || data.type} · ${data.gender} ${data.isNPC ? '[НПС]' : ''}
                </div>
        `;
        buttonsContainer.appendChild(button);
    }
    attachButtonHandlers();
}

function generateCards() {
    cardsContainer = document.getElementById('cardsContainer');
    if (!cardsContainer) return;
    cardsContainer.innerHTML = '';
    
    const charKeys = Object.keys(charactersData);
    
    for (let key of charKeys) {
        const data = charactersData[key];
        const descriptionHtml = data.description.split('\n').map(p => `<p>${p}</p>`).join('');
        
        const card = document.createElement('div');
        card.className = 'character-card';
        card.id = `${key}-card`;
        
        let rightColumnContent = '';
        if (data.portraitBg && (data.portraitBg.endsWith('.jpg') || data.portraitBg.endsWith('.png') || data.portraitBg.endsWith('.gif'))) {
            rightColumnContent = `<img src="${data.portraitBg}" alt="${data.buttonName}" class="character-portrait">`;
        } else {
            rightColumnContent = `<div class="portrait-placeholder" style="background: ${data.portraitBg || '#1a0202'}"></div>`;
        }
        
        
        let linksHtml = '';
        if (!data.isNPC) {
            const albumLink = data.albumLink ? data.albumLink : '#';
            const discussionLink = data.discussionLink ? data.discussionLink : '#';
            const loreLink = data.loreLink ? data.loreLink : '#';
            
            linksHtml = `
                <div class="links-placeholder">
                    <a href="${albumLink}" target="_blank" class="dummy-link character-link">АЛЬБОМ</a>
                    <a href="${discussionLink}" target="_blank" class="dummy-link character-link">ОБСУЖДЕНИЕ</a>
                </div>
            `;
        } else {
            linksHtml = `
                <div class="links-placeholder npc-links">
                    <div class="dummy-link npc-disabled">ИНФОРМАЦИЯ ЗАШИФРОВАНА</div>
                </div>
            `;
        }
        
        card.innerHTML = `
            <div class="card-left">
                <div class="card-header">
                    <div class="card-title">${data.buttonName}</div>
                </div>
                <div class="character-tags">
                    <div class="tag"><span>${data.displayType || data.type}</span></div>
                    <div class="tag"><span>${data.gender}</span></div>
                    <div class="tag location-tag"><span>${data.locationLabel}</span></div>
                    ${data.isNPC ? '<div class="tag" style="background:#3a0a0a;"><span>НПС</span></div>' : ''}
                </div>
                <div class="card-description">${descriptionHtml}<small>⛧ собственность caro libera ⛧</small></div>
                ${linksHtml}
            </div>
            <div class="card-right">
                ${rightColumnContent}
            </div>
            <div class="close-button">✕</div>
        `;
        cardsContainer.appendChild(card);
    }
}

function closeAllCards() {
    if (isClosing) return;
    isClosing = true;
    const cards = document.querySelectorAll('.character-card');
    cards.forEach(card => {
        if (card.classList.contains('active')) {
            card.classList.add('closing');
            card.classList.remove('active');
        }
    });
    if (backButton) {
        backButton.classList.remove('visible');
    }
    setTimeout(() => {
        cards.forEach(card => card.classList.remove('closing'));
        isClosing = false;
        activeCard = null;
    }, 250);
}

function attachButtonHandlers() {
    const buttons = document.querySelectorAll('.character-button');
    const closeBtns = document.querySelectorAll('.close-button');
    
    buttons.forEach(btn => {
        if (btn._clickHandler) btn.removeEventListener('click', btn._clickHandler);
        const clickHandler = function(e) {
            e.preventDefault();
            e.stopPropagation();
            if (isClosing) return;
            const charId = this.getAttribute('data-character');
            const targetCard = document.getElementById(`${charId}-card`);
            if (!targetCard) return;
            if (targetCard.classList.contains('active')) { closeAllCards(); return; }
            const allCards = document.querySelectorAll('.character-card');
            allCards.forEach(c => {
                if (c.classList.contains('active')) {
                    c.classList.add('closing');
                    c.classList.remove('active');
                }
            });
            setTimeout(() => {
    allCards.forEach(c => c.classList.remove('closing'));
    targetCard.classList.add('active');
    activeCard = targetCard;
    if (backButton) {
        backButton.classList.add('visible');
    }
    
    
    const charId = this.getAttribute('data-character');
    const characterData = charactersData[charId];
    if (characterData && characterData.location) {
        changeBackgroundByLocation(characterData.location);
    }
}, 150);
        };
        btn.addEventListener('click', clickHandler);
        btn._clickHandler = clickHandler;
    });
    
    closeBtns.forEach(btn => {
        if (btn._closeHandler) btn.removeEventListener('click', btn._closeHandler);
        const closeHandler = (e) => { e.stopPropagation(); closeAllCards(); };
        btn.addEventListener('click', closeHandler);
        btn._closeHandler = closeHandler;
    });
    
    document.querySelectorAll('.character-card').forEach(card => {
        if (card._stopProp) card.removeEventListener('click', card._stopProp);
        card.addEventListener('click', (e) => e.stopPropagation());
        card._stopProp = true;
    });
}

function updateFilterButtonsStyle() {
    document.querySelectorAll('.filter-sub-btn').forEach(btn => {
        const filterType = btn.getAttribute('data-filter');
        const filterValue = btn.getAttribute('data-value');
        btn.classList.remove('active-filter');
        if (filterType && activeFilters[filterType] && activeFilters[filterType] === filterValue) {
            btn.classList.add('active-filter');
        }
    });
    
    const npcBtn = document.getElementById('npcToggleBtn');
    if (npcBtn) {
        if (activeFilters.npc === "characters") npcBtn.innerHTML = `ПЕРСОНАЖИ ▼`;
        else if (activeFilters.npc === "npcs") npcBtn.innerHTML = `НПС ▼`;
        else npcBtn.innerHTML = `ВСЕ ▼`;
        npcBtn.classList.add('has-filter');
    }
    
    const genderBtn = document.querySelector('.filter-group:first-child .filter-main-btn');
    const raceBtn = document.querySelector('.filter-group:nth-child(2) .filter-main-btn');
    const locBtn = document.querySelector('.filter-group:nth-child(3) .filter-main-btn');
    
    if (genderBtn) {
        if (activeFilters.gender) {
            let icon = activeFilters.gender === "мужской" ? "♂" : (activeFilters.gender === "женский" ? "♀" : "⚧");
            genderBtn.innerHTML = `ПОЛ: ${icon} ▼`;
            genderBtn.classList.add('has-filter');
        } else { genderBtn.innerHTML = `ПОЛ ▼`; genderBtn.classList.remove('has-filter'); }
    }
    if (raceBtn) {
        if (activeFilters.race) {
            let icon = activeFilters.race === "человек" ? "👤" : "🐺";
            raceBtn.innerHTML = `РАСА: ${icon} ▼`;
            raceBtn.classList.add('has-filter');
        } else { raceBtn.innerHTML = `РАСА ▼`; raceBtn.classList.remove('has-filter'); }
    }
    if (locBtn) {
        if (activeFilters.location) {
            let icon = "📍";
            if (activeFilters.location === "город") icon = "🏙️";
            else if (activeFilters.location === "сектор") icon = "⚙️";
            else if (activeFilters.location === "элита") icon = "👑";
            else if (activeFilters.location === "безопасность") icon = "🛡️";
            locBtn.innerHTML = `МЕСТО: ${icon} ▼`;
            locBtn.classList.add('has-filter');
        } else { locBtn.innerHTML = `МЕСТО ▼`; locBtn.classList.remove('has-filter'); }
    }
}

function initFilters() {
    const groups = document.querySelectorAll('.filter-group');
    const btns = document.querySelectorAll('.filter-sub-btn');
    const resetBtn = document.getElementById('resetAllFilters');
    
    groups.forEach(group => {
        const main = group.querySelector('.filter-main-btn');
        main.addEventListener('click', (e) => {
            e.stopPropagation();
            groups.forEach(g => { if (g !== group) g.classList.remove('active'); });
            group.classList.toggle('active');
        });
    });
    
    btns.forEach(btn => {
        btn.addEventListener('click', (e) => {
            e.stopPropagation();
            const type = btn.getAttribute('data-filter');
            const val = btn.getAttribute('data-value');
            if (type && val !== undefined) {
                if (activeFilters[type] === val) activeFilters[type] = "";
                else activeFilters[type] = val;
            }
            groups.forEach(g => g.classList.remove('active'));
            
            if (type === "location") {
                changeBackgroundByLocation(activeFilters.location);
            }
            
            updateFilterButtonsStyle();
            renderButtons();
        });
    });
    
    if (resetBtn) {
        resetBtn.addEventListener('click', () => {
            activeFilters = { gender: "", race: "", location: "", npc: "characters" };
            groups.forEach(g => g.classList.remove('active'));
            changeBackgroundByLocation(null);
            updateFilterButtonsStyle();
            renderButtons();
        });
    }
    document.addEventListener('click', () => groups.forEach(g => g.classList.remove('active')));
    updateFilterButtonsStyle();
}

