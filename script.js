function initSearch() {
    const searchInput = document.getElementById('searchInput');
    const searchResults = document.getElementById('searchResults');
    
    if (!searchInput || !searchResults) return;
    
    function updateSearchResults(query) {
        if (query.length === 0) {
            searchResults.classList.remove('active');
            return;
        }
        
        const allCharacters = Object.keys(charactersData);
        const lowerQuery = query.toLowerCase();
        
        const matches = allCharacters.filter(key => {
            const data = charactersData[key];
            return data.buttonName.toLowerCase().includes(lowerQuery);
        });
        
        if (matches.length > 0) {
            searchResults.innerHTML = matches.map(key => {
                const data = charactersData[key];
                return `
                    <div class="search-result-item" data-character="${key}">
                        <span class="search-result-name">${data.buttonName}</span>
                        <span class="search-result-type">${data.type} · ${data.gender}</span>
                        ${data.isNPC ? '<span class="tag-badge">НПС</span>' : ''}
                    </div>
                `;
            }).join('');
            searchResults.classList.add('active');
            
            document.querySelectorAll('.search-result-item[data-character]').forEach(item => {
                item.addEventListener('click', () => {
                    const charKey = item.getAttribute('data-character');
                    const button = document.querySelector(`.character-button[data-character="${charKey}"]`);
                    if (button) {
                        button.click();
                        searchInput.value = '';
                        searchResults.classList.remove('active');
                    }
                });
            });
        } else {
            searchResults.innerHTML = '<div class="no-results">🔍 Ничего не найдено</div>';
            searchResults.classList.add('active');
        }
    }
    
    let debounceTimer;
    searchInput.addEventListener('input', (e) => {
        clearTimeout(debounceTimer);
        debounceTimer = setTimeout(() => {
            updateSearchResults(e.target.value);
        }, 200);
    });
    
    document.addEventListener('click', (e) => {
        if (!searchInput.contains(e.target) && !searchResults.contains(e.target)) {
            searchResults.classList.remove('active');
        }
    });
    
    searchInput.addEventListener('keydown', (e) => {
        if (e.key === 'Escape') {
            searchInput.value = '';
            searchResults.classList.remove('active');
        }
        if (e.key === 'Enter') {
            const firstResult = searchResults.querySelector('.search-result-item');
            if (firstResult) {
                firstResult.click();
            }
        }
    });
}

let mouseX = 0, mouseY = 0, targetX = 0, targetY = 0, currentX = 0, currentY = 0, parallaxAnimationId = null;

function onMouseMove(e) {
    mouseX = (e.clientX / window.innerWidth) * 2 - 1;
    mouseY = (e.clientY / window.innerHeight) * 2 - 1;
    targetX = mouseX * 15;
    targetY = mouseY * 15;
}

function updateParallax() {
    currentX += (targetX - currentX) * 0.08;
    currentY += (targetY - currentY) * 0.08;
    const maxOffset = 5;
    const limitedX = Math.min(Math.max(currentX, -maxOffset), maxOffset);
    const limitedY = Math.min(Math.max(currentY, -maxOffset), maxOffset);
    
    if (backgroundElement) {
        backgroundElement.style.transform = `translate(${limitedX}px, ${limitedY}px)`;
    }
    parallaxAnimationId = requestAnimationFrame(updateParallax);
}

function startParallax() {
    if ('ontouchstart' in window) return;
    window.addEventListener('mousemove', onMouseMove);
    parallaxAnimationId = requestAnimationFrame(updateParallax);
}

